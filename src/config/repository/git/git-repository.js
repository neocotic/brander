'use strict';

/* istanbul ignore file */

const Repository = require('../repository');

const _host = Symbol('host');

/**
 * A {@link Repository} implementation for Git.
 *
 * @public
 */
class GitRepository extends Repository {

  /**
   * Creates an instance of {@link GitRepository} based on the <code>host</code> provided.
   *
   * <code>host</code> is expected to be generated by the <code>hosted-git-info</code> module.
   *
   * @param {GitHost} host - the host information to be used
   * @public
   */
  constructor(host) {
    super('git');

    this[_host] = host;
  }

  /**
   * @inheritdoc
   * @override
   */
  getBranch() {
    return this[_host].committish || 'master';
  }

  /**
   * @inheritdoc
   * @override
   */
  getFileURL(filePath, fragment) {
    return this[_host].browse(filePath, fragment);
  }

  /**
   * @inheritdoc
   * @override
   */
  getHomepage() {
    return this[_host].browse();
  }

  /**
   * @inheritdoc
   * @override
   */
  getName() {
    return this[_host].project;
  }

  /**
   * @inheritdoc
   * @override
   */
  getRawFileURL(filePath) {
    return this[_host].file(filePath);
  }

  /**
   * @inheritdoc
   * @override
   */
  getURL() {
    return this[_host].https();
  }

  /**
   * @inheritdoc
   * @override
   */
  getUser() {
    return this[_host].user;
  }

}

module.exports = GitRepository;
